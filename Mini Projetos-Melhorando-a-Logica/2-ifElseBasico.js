//if..else
//switch case

//se a hora estiver entre 06:00 ate 12:00 bom dia
//se estiver entre 12:00 até as 18:00 boa tarde
//caso contrário 18:00 boa noite
let hora = 20
if (hora > 6 && hora < 12) {
  console.log('Bom dia')
}
else if
  (hora > 12 && hora < 18) {
  console.log('Boa tarde')
}
else {
  console.log('boa noite')
}
